think it to be. If you believe that school is not necessary or fundamental to your success, leave. But if
you believe that it is necessary, stay. Don’t get me wrong though, I’m not asking you to drop out of
school, instead, <b>I’m asking you to find a reason to stay in school</b>. Do you find yourself always craving to
stay off reality, then check, you are not being true to yourself.
