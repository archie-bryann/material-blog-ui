<div id = "me" class = "w3-container">
I remember being fairly happy when I finally resumed after the long-awaited call from The University of Ibadan. Everyone was around me – my mom, my brother, relatives were all so happy. I replied every compliment and comment with a smile but deep down, I wasn’t very happy. I was perplexed.
</div>
<br>
<div id = "me" class = "w3-container">
“Who are you?” someone asked me in JSS 3. At this glorious point, one of the most important checkpoint in my life, I still had no answer.
</div>
<br>
<div id = "me" class = "w3-container">
Let me ask you this: Who are you?
</div>
<br>
<div id = "me" class = "w3-container">
I kept ignoring that question by escaping reality. I just kept on studying for school examinations and passing, from one exam to another, to another, to another.
</div>
<br>
<div id = "me" class = "w3-container">
“Will it ever stop?” I asked my mom. “I never got the answer I wanted.
</div>
<br>
<div id = "me" class = "w3-container">
”In life, we never stop learning”, she said.
</div>
<br>
<div id = "me" class = "w3-container">
If I had a doubt or was in conflict with myself, I simply turned up the sound of my headphone and stopped thinking, I only listened to the voice of the artist and slowly silenced my inner voice until all I kept hearing was – “You must pass your exams”, “You must not fail”. Unless you want to deceive yourself, in this present life, you know that you cannot think for yourself. The society thinks for you. We are forced to learn the status quo and we are not even aware of it. We are programmed into the way of the society. We are forced to live in the dogma of people’s thought, with the result of other people’s thinking. Okay, you think I’m crazy, let me ask you this question – if there was no such thing as a school, were would you be? What would you be doing? Who would you be? Who are you?
</div>
<br>
<div id = "me" class = "w3-container">
Do you see what I mean? Some of us just sleep walk through life? Some of us don’t have a purpose. I remember I took a survey 2 years ago. The survey was targeted towards university school students. One vital question asked there was: What do you aim to achieve in the next few years? 69% wrote that they wanted to earn a degree and we asked then why, only 20% answered why. It’s nice to get a degree but what do you want to do with your life? At this point I’m messing with your mind, you are probably confused. But only the courageous will read this to the end and see the truth.
</div>
<br>
<div id = "me" class = "w3-container">
I asked myself who I was and never got an answer. I took a month off my school work, I stopped moving with the crowd. I stopped listening to the society’s voice. Why? Look around you, look at your parents, look at your lecturers for goodness sake, open your fucking eyes and see the truth. Some people, they get a degree, they earn a living and then they die. You don’t know their name. You don’t know whether they made a difference in the world. And it doesn’t matter if you know or not, they never actually made a difference in this indifferent world.
</div>
<br>
<div id = "me" class = "w3-container">
Some of us just keeping running away from the doubts in our mind by continually listening the music, watching movies, staring at your phones, watching porn, doing drugs – they are all the same things, keeping our minds in a state of rest. In a state where it cannot work, all it can do is abide by the laid down rules. Why do you think people spend a lot of money trying to watch the avengers HD? The
avengers is the best movie to watch if you want to escape reality and in HD, you are blown away. We
look for illusions, because the reality is not how we want it to be. That’s why the entertainment industry
will never die, they fill our head with what is not real, and we love it and are willing to pay a lot for it.
</div>
<br>
<div id = "me" class = "w3-container">
In reality, we have millions of questions flying through our mind, people we wish to criticize, structures
and frameworks we wish to question, but we can’t, why? We are taught from home to adjust to
whatever difficulty comes our way, to whatever itches. I listened to my inner voice and decided to drop
out of school. Everyone, my parents, relatives, the school, kept telling me to stay in school. They listed
one thousand and one reasons why I should stay in school. With one of their reason which they
frequently used as leverage being that I should not hurt my parents. But in reality, what was their
reason? Beneath their many words called reasons, all I heard is: “Stay in school, because I want you to”.
</div>
<br>
<div id = "me" class = "w3-container">
Within few weeks in the process of defending my decision, the society responded back with sharp
piercing nail of stigma, they thought I wouldn’t notice, but my roommates began to ignore me, they
were subtle about it, but I know it wasn’t intentional. They would avoid eye contact, avoid any
conversation with me. I could say that, the society was talking back at me.
</div>
<br>
<div id = "me" class = "w3-container">
I lived with that for a month. I dedicated a whole month for self-reflection. Who am I? I asked myself
every single day. I prayed too. I did not go to class or even church. The church is the backbone of the
society. We’ll probably talk about that later. I just kept searching my soul and tuned up my inner voice.
The voice that the society wanted dead. You still wandering what the society is, don’t let school deceive
you – <b>the society is everyone minus you</b>. I remember when talking to a counsellor, they thought I was
crazy. He boasted of how his children travelled abroad and did excellently well.
</div>
<br>
<div id = "me" class = "w3-container">
“Why do people keep running abroad, why do they loathe Nigeria”, I asked.
</div>
<br>
<div id = "me" class = "w3-container">
He was perplexed. He thought that I would be the type to rejoice to him. Surprisingly, he told me to
explain. I gave him a scenario.
</div>
<br>

<div id = "me" class = "w3-container">
<b>The society is the mindset of the country.</b>
</div>
<br>


<div id = "me" class = "w3-container">
“Imagine if you take every citizen in America put them in Nigeria, and every citizen in Nigeria put them
in America. Tell me what would happen to America and what would happen to Nigeria?” I asked. The
counsellor was surprised. The answer – Nigeria would be great, America would be one of the worst
countries. Stop deceiving yourself with the excuse that Nigeria is bad. It’s you that is bad, you let the
society convince you into thinking you have a small mind, you become a small thinker and you are
convinced that you cannot do anything for your country. You, therefore, run away from your country
and remake yourself into becoming the white man’s slave, working your ass out for them to survive
while they rest at home spending the most of their time with their family. There’s nothing wrong with
the society, it’s you.
</div>
<br>

<div id = "me" class = "w3-container">
I see many students after examinations, they leave their books and go to watch TV. They don’t know
who wrote those books, they don’t know if the person is successful or not. They don’t know what they
are reading. They think they forget all what they studied after examinations, the messages from the
classes and the books are stored in their sub-conscious - but they don’t know. You spend significant
years of your life in school, go get a job and work for someone. Don’t get me wrong, those are what
some want, but there are others that don’t want or who don’t know what they want but they keep
going in the same direction as everyone because they have been programmed that way.
</div>
<br>

<div id = "me" class = "w3-container">
Albert Einstein once said, “<b>Imagination</b> and Knowledge are important, but knowledge empowers
<b>imagination</b>”. Why would the great Albert Einstein still give credit to imagination? Because it was given
to us for a reason. God doesn’t make mistakes in his design – I believe that. But we were taught in
school to forsake that at childhood and rely on knowledge. You can’t even write an essay the way you
want it. You have to write it the way the society wants it. Then how can you be great? 55% of the
economy in America is run by private companies (e.g. Apple, Microsoft etc.), that means more than half
of the economy is run by creative individuals, what else would a country ever need to be successful. A
country will always be full of corruption, criminal activities, etc. but that never stops that country from
being successful. The reason why Nigeria is bad as you say, is because you are bad for this country. So if
you want to leave, get the fuck out and become a slave of the white man’s country, where God did not
place you and keep posting fake pictures of you being happy when you are not.
</div>
<br>

<div id = "me" class = "w3-container">
<b>Albert Einstein used only 5% of his brain, what percentage have you used?</b>
</div>
<br>

<div id = "me" class = "w3-container">
Stop doing and start thinking. Henry Ford once said, “Thinking is the hardest job there is, that why few
engage in it.” Serious people that drop out of school don’t drop out because they are lazy, they drop out
because they become fed up with the society dictating their every move and they attempt something
new. Now, the small thinkers or the society cast them aside, and make sure that it is extra hard for them
to make it. Only a few succeed to tell the tale. The others, on the other hand, are continually spat upon
by the society, they are looked down upon by <b>you</b>.
</div>
